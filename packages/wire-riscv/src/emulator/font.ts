/**
 * Get the bitmap data for a single character
 */
export function getCharBitmap(charCode: number): Uint8Array {
  const code = charCode & 0x7F; // Limit to 0-127
  const offset = code * FONT_HEIGHT;
  return FONT_DATA.subarray(offset, offset + FONT_HEIGHT);
}

/**
 * Check if a pixel is set in a character glyph
 */
export function isPixelSet(charCode: number, x: number, y: number): boolean {
  if (x < 0 || x >= FONT_WIDTH || y < 0 || y >= FONT_HEIGHT) {
    return false;
  }
  const bitmap = getCharBitmap(charCode);
  const row = bitmap[y];
  // MSB is leftmost pixel
  return (row & (0x80 >> x)) !== 0;
}
