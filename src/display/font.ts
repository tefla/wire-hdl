/**
 * Get font data for a character
 */
export function getCharData(ascii: number): Uint8Array {
  if (ascii < FIRST_CHAR || ascii > LAST_CHAR) {
    ascii = 32; // Default to space
  }
  const offset = (ascii - FIRST_CHAR) * CHAR_HEIGHT;
  return FONT_DATA.subarray(offset, offset + CHAR_HEIGHT);
}
